int	ft_str_is_numeric(char *str)
{
	int	rtn;
	int	i;

	rtn = 1;
	i = 0;
	while (str[i] != '\0' && rtn != 0)
	{
		if (str[i] < '0' || str[i] > '9')
			rtn = 0;
		i++;
	}
	return (rtn);
}
