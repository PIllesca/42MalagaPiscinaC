#include <unistd.h>

void	draw_matrix(char **mat, int *xy)
{
	int	i;
	int	j;

	i = 0;
	while (i < xy[1])
	{
		j = 0;
		while (j < xy[0])
		{
			write (1, &mat[i][j], 1);
			j++;
		}
		write (1, "\n", 1);
		i++;
	}
}
